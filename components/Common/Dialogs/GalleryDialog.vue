<script setup>
import { ref, computed, watch, onMounted, onUnmounted, nextTick } from 'vue';
import { Swiper, SwiperSlide } from 'swiper/vue';
import { Navigation } from 'swiper/modules';
import 'swiper/css';
import 'swiper/css/navigation';

const props = defineProps({
  modelValue: {
    type: Boolean,
    default: false,
  },
  slide: {
    type: Object,
    default: null,
  },
  closeOnOverlay: {
    type: Boolean,
    default: true,
  },
});

const emit = defineEmits(['update:modelValue']);

const currentIndex = ref(0);
const prevButtonRef = ref(null);
const nextButtonRef = ref(null);
const swiperInstance = ref(null);
const isMobile = ref(false);

const totalSlides = computed(() => props.slide?.gallery?.length || 0);
const visibleSlidesCount = 2;

const isPrevDisabled = computed(() => currentIndex.value <= 0);
const isNextDisabled = computed(() => {
  if (totalSlides.value <= visibleSlidesCount) return true;
  return currentIndex.value >= totalSlides.value - visibleSlidesCount;
});

const progressPercentage = computed(() => {
  if (totalSlides.value === 0) return 0;
  if (totalSlides.value <= visibleSlidesCount) return 100;
  const maxIndex = totalSlides.value - visibleSlidesCount;
  return ((currentIndex.value + visibleSlidesCount) / totalSlides.value) * 100;
});

const onSwiper = swiper => {
  swiperInstance.value = swiper;
};

const onSlideChange = swiper => {
  currentIndex.value = swiper.activeIndex;
};

// Функция для определения мобильного устройства
const checkIsMobile = () => {
  if (process.client) {
    isMobile.value = window.innerWidth <= 599;
  }
};

const handleKeydown = event => {
  if (!props.modelValue) return;

  switch (event.key) {
    case 'Escape':
      emit('update:modelValue', false);
      break;
    case 'ArrowLeft':
      if (swiperInstance.value && !isPrevDisabled.value) {
        swiperInstance.value.slidePrev();
      }
      break;
    case 'ArrowRight':
      if (swiperInstance.value && !isNextDisabled.value) {
        swiperInstance.value.slideNext();
      }
      break;
  }
};

// Композабл для работы с Lenis
const { stop: stopScroll, start: startScroll } = useLenis();

const lockScroll = () => {
  stopScroll(); // Останавливаем Lenis скролл
};

const unlockScroll = () => {
  startScroll(); // Возобновляем Lenis скролл
};

watch(
  () => props.modelValue,
  async newValue => {
    if (newValue) {
      currentIndex.value = 0;
      lockScroll();

      if (process.client) {
        const header = document.querySelector('.header');
        if (header) {
          header.style.transform = 'translateY(-100%)';
          header.style.transition = 'transform 0.3s ease';
        }
      }

      await nextTick();
      if (swiperInstance.value) {
        swiperInstance.value.slideTo(0);
      }
    } else {
      unlockScroll();

      if (process.client) {
        const header = document.querySelector('.header');
        if (header) {
          header.style.transform = '';
        }
      }
    }
  }
);

watch(
  () => props.slide?.gallery?.length,
  () => {
    currentIndex.value = 0;
    if (swiperInstance.value) {
      swiperInstance.value.slideTo(0);
    }
  }
);

onMounted(() => {
  if (typeof document !== 'undefined') {
    document.addEventListener('keydown', handleKeydown);
    window.addEventListener('resize', checkIsMobile);
    checkIsMobile(); // Проверяем при монтировании
  }
});

onUnmounted(() => {
  if (typeof document !== 'undefined') {
    document.removeEventListener('keydown', handleKeydown);
    window.removeEventListener('resize', checkIsMobile);
  }
  unlockScroll();

  if (process.client) {
    const header = document.querySelector('.header');
    if (header) {
      header.style.transform = '';
    }
  }
});
</script>

